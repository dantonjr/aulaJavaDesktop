package edu.senai.a22.testes.calculadora;

public class Calculadora {
	public long getAdicao(long v1, long v2) {
		return v1 + v2;
	}

	public long getSubtracao(long v1, long v2) {
		return v1 - v2;
	}

	public long getDivisao(long v1, long v2) {
		return v1 / v2;
	}

	public long getMultiplicacao(long v1, long v2) {
		return v1 * v2;
	}
}
